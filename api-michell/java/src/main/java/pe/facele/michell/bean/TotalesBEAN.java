package pe.facele.michell.bean;

import java.math.BigDecimal;

public class TotalesBEAN {

	BigDecimal importeTotal;
	BigDecimal valorGravado;
	BigDecimal valorExonerado;
	BigDecimal valorInafecto;
	BigDecimal valorExportacion;
	BigDecimal valorGratuito;
	
	public BigDecimal getImporteTotal() {
		return importeTotal;
	}
	public void setImporteTotal(BigDecimal importeTotal) {
		this.importeTotal = importeTotal;
	}
	public BigDecimal getValorGravado() {
		return valorGravado;
	}
	public void setValorGravado(BigDecimal valorGravado) {
		this.valorGravado = valorGravado;
	}
	public BigDecimal getValorExonerado() {
		return valorExonerado;
	}
	public void setValorExonerado(BigDecimal valorExonerado) {
		this.valorExonerado = valorExonerado;
	}
	public BigDecimal getValorInafecto() {
		return valorInafecto;
	}
	public void setValorInafecto(BigDecimal valorInafecto) {
		this.valorInafecto = valorInafecto;
	}
	public BigDecimal getValorExportacion() {
		return valorExportacion;
	}
	public void setValorExportacion(BigDecimal valorExportacion) {
		this.valorExportacion = valorExportacion;
	}
	public BigDecimal getValorGratuito() {
		return valorGratuito;
	}
	public void setValorGratuito(BigDecimal valorGratuito) {
		this.valorGratuito = valorGratuito;
	}
	
}
